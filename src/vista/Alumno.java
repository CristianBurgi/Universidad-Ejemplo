    private void btnModificarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarAlumnoActionPerformed
        
         //obtengo los valores de los jtextfield y los guardo en variables...
        String id = txtID.getText();
        String dni = txtDni.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();

        //forma de pasar a String el date del JDATECHOOSER!
        String fechaNac = ((JTextField) date.getDateEditor().getUiComponent()).getText();

        String estado = txtEstado.getText();
        
        String consulta = "update alumno set dni ='" + dni + "', "
                    + "nombre = '" + nombre + "', apellido = '"+apellido+"', fechaNacimiento = '"+fechaNac+"', estado = '"+estado+"' where idAlumno = '" + id+ "'";
        
        
        try {
            
            PreparedStatement ps = cn.prepareStatement(consulta);

            int indice = ps.executeUpdate();

            if (indice > 0) {

                JOptionPane.showMessageDialog(this, "Datos Actualizados Correctamente");
               
                
            } else {
                JOptionPane.showMessageDialog(this, "No seleccionó ninguna fila");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error " + e);
        }
        

    }//GEN-LAST:event_btnModificarAlumnoActionPerformed


