    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        //obtengo los valores de los jtextfield y los guardo en variables...
        
        String dni = txtDni.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        //forma de pasar a String el date del JDATECHOOSER!
        String fechaNac = ((JTextField) date.getDateEditor().getUiComponent()).getText();
        String estado = txtEstado.getText();
        
//        validarDNI(dni);
        
        if (dni.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || fechaNac.isEmpty() || estado.isEmpty()) {
            JOptionPane.showMessageDialog(this, "rellene todos los campos");
            
        }else{

        //consulta que va a ser enviada
        String sql = "INSERT INTO alumno (dni,nombre,apellido,fechanacimiento,estado) VALUES (?,?,?,?,?)";
        try {

            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setString(1, dni);
            ps.setString(2, nombre);
            ps.setString(3, apellido);
            //seteo Date en la BD parseando el String a Date.  
            ps.setDate(4, Date.valueOf(fechaNac));
            ps.setString(5, estado);

            ps.execute();
            JOptionPane.showMessageDialog(this, "Ingreso Exitoso");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "No se cargó correctamente " + e);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed
    }
